package com.esmt.formation.java;

public class Classe  {
	
	private String nom;
	private String salle;
	
	public Classe(String nom, String salle) {
		super();
		this.nom = nom;
		this.salle = salle;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public String getSalle() {
		return salle;
	}

	public void setSalle(String salle) {
		this.salle = salle;
	}
	
	

}
